def addition(a,b):
    addition = a+b
    return addition


def subtract(a,b):
    sub = a-b
    return sub

def multiply(a,b):
    mul = a*b
    return mul

def divide(a,b):
    div = a/b
    return div

def exp(a,b):
    expp = a**b
    return expp

addresult = addition(1,2)
print(addresult)

